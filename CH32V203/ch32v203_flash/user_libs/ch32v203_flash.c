void flash_unlock_option_bytes(void)
{
	FLASH->KEYR = FLASH_KEY1;
	FLASH->KEYR = FLASH_KEY2;
	FLASH->OBKEYR = FLASH_KEY1;
	FLASH->OBKEYR = FLASH_KEY2;
}

//HINT: The flash controller uses only the low byte, the high byte is always the complement of the low byte.
void flash_write_option_bytes(uint32_t address, uint16_t val)
{
	FLASH->CTLR = (FLASH->CTLR & ~(FLASH_CTLR_PG | FLASH_CTLR_PER | FLASH_CTLR_MER | FLASH_CTLR_OPTPG | FLASH_CTLR_OPTER | FLASH_CTLR_PAGE_PG | FLASH_CTLR_PAGE_ER | FLASH_CTLR_PAGE_BER32 | FLASH_CTLR_PAGE_BER64)) | FLASH_CTLR_OPTPG;
	*(volatile uint16_t *)address = val;
	while(FLASH->STATR & FLASH_STATR_BSY);
	FLASH->CTLR &= ~(FLASH_CTLR_PG | FLASH_CTLR_PER | FLASH_CTLR_MER | FLASH_CTLR_OPTPG | FLASH_CTLR_OPTER | FLASH_CTLR_PAGE_PG | FLASH_CTLR_PAGE_ER | FLASH_CTLR_PAGE_BER32 | FLASH_CTLR_PAGE_BER64);
}

